a =10
b = 4

# Print bitwise AND operation
print('a&b - ',a&b)

# Print bitwise OR operation
print('a|b - ',a|b)

# Print bitwise NOT operation 
print('Once complement of ~a - ',~a)
print('Once complement of ~b - ',~b)

# print bitwise XOR operation   
print("a ^ b =", a ^ b) 

# print bitwise shift operator divide the number by 2 if 15>>1
print('15 >> 3 - ',15>>3)

# print bitwise shift operator multiply the number by 2 if 15>>1
print('15 << 3 - ',15<<3)